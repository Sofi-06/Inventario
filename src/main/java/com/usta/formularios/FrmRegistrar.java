    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        if (estaTodoBien()) {
            String nombre, apellido, correo, documento, telefono, usuario, contrasenna, acceso;
            nombre = txtNombre.getText();
            apellido = txtApellido.getText();
            documento = txtDocumento.getText();
            correo = txtCorreo.getText();
            telefono = txtTelefono.getText();
            usuario = txtUsuario.getText();
            contrasenna = txtContraseña.getText();
            acceso = cmbRol.getSelectedItem().toString();

            Persona objPersona = new Persona();

            objPersona.setNombrePersona(nombre);
            objPersona.setApellidoPersona(apellido);
            objPersona.setDocumentoPersona(documento);
            objPersona.setCorreoPersona(correo);
            objPersona.setTelefonoPersona(telefono);
            objPersona.setUsuarioPersona(usuario);
            objPersona.setContrasennaPersona(contrasenna);
            objPersona.setNivelAccesoPersona(acceso);

            DaoPersona DaoPers = new DaoPersona();

            if (DaoPers.registrar(objPersona)) {
                JOptionPane.showMessageDialog(PanelRegistrar, "Registro exitoso", "Información", JOptionPane.INFORMATION_MESSAGE);
                FrmPrincipal login = new FrmPrincipal();
                login.setVisible(true);
                this.setVisible(false);
                limpiar();
            } else {
                JOptionPane.showMessageDialog(PanelRegistrar, "No se pudo resgistrar", "Error", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_btnIngresarActionPerformed


    private Boolean estaTodoBien() {
        String nombre, apellido, documento, correo, telefono, usuario, contrasenna;

        Boolean bandera = true;
        nombre = txtNombre.getText();
        if (nombre.equals("")) {
            txtNombre.requestFocus();
            bandera = false;
            JOptionPane.showMessageDialog(PanelRegistrar, "Digite su nombre", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            apellido = txtApellido.getText();
            if (apellido.equals("")) {
                txtApellido.requestFocus();
                bandera = false;
                JOptionPane.showMessageDialog(PanelRegistrar, "Digite su apellido", "Advertencia", JOptionPane.WARNING_MESSAGE);
            } else {
                correo = txtCorreo.getText();
                if (correo.equals("")) {
                    txtCorreo.requestFocus();
                    bandera = false;
                    JOptionPane.showMessageDialog(PanelRegistrar, "Digite su correo", "Advertencia", JOptionPane.WARNING_MESSAGE);
                } else {
                    telefono = txtTelefono.getText();
                    if (telefono.equals("")) {
                        txtTelefono.requestFocus();
                        bandera = false;
                        JOptionPane.showMessageDialog(PanelRegistrar, "Digite su teléfono", "Advertencia", JOptionPane.WARNING_MESSAGE);
                    } else {
                        usuario = txtUsuario.getText();
                        if (usuario.equals("") || !usuario.equals(txtCorreo.getText())) {
                            txtUsuario.requestFocus();
                            bandera = false;
                            JOptionPane.showMessageDialog(PanelRegistrar, "Digite el usuario con el que va a ingresar al sistema, debe ser igual al correo", "Advertencia", JOptionPane.WARNING_MESSAGE);
                        } else {
                            contrasenna = txtContraseña.getText();
                            if (contrasenna.equals("")) {
                                txtContraseña.requestFocus();
                                bandera = false;
                                JOptionPane.showMessageDialog(PanelRegistrar, "Digite la contraseña con la que va a ingresar al sistema", "Advertencia", JOptionPane.WARNING_MESSAGE);
                            } else {
                                if (cmbRol.getSelectedIndex() == 0) {
                                    bandera = false;
                                    JOptionPane.showMessageDialog(PanelRegistrar, "Por favor seleccione un rol", "Advertencia", JOptionPane.WARNING_MESSAGE);
                                } else {
                                    documento = txtDocumento.getText();
                                    if (documento.equals(" ")) {
                                        txtDocumento.requestFocus();
                                        bandera = false;
                                        JOptionPane.showMessageDialog(PanelRegistrar, "Digite su documento", "Advertencia", JOptionPane.WARNING_MESSAGE);
                                    }
                                }

                            }
                        }

                    }

                }
            }

        }
        return bandera;

    }


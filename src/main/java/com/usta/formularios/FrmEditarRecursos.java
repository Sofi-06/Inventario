    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (estaTodoBien()) {
            String nombre, codInv, referencia;
            Integer cantidad;
            nombre = txtNombre.getText();
            codInv = txtCodigo.getText();
            referencia = txtReferencia.getText();
            cantidad = Integer.valueOf(txtCantidad.getText());

            objActualizar.setNombreElemento(nombre);
            objActualizar.setInventarioElemento(codInv);
            objActualizar.setCantidadElemento(cantidad);
            objActualizar.setReferenciaElemento(referencia);

            DaoElemento DaoElemento = new DaoElemento();

            if (DaoElemento.actualizar(objActualizar)) {
                JOptionPane.showMessageDialog(panelCuerpo, "Registro exitoso", "Información", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            } else {
                JOptionPane.showMessageDialog(panelCuerpo, "No se pudo resgistrar", "Error", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_btnEditarActionPerformed


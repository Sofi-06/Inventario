    private Boolean estaTodoBien() {
        String nombre, codInv, referencia;
        Integer cantidad;

        Boolean bandera = true;
        nombre = txtNombre.getText();
        if (nombre.equals("")) {
            txtNombre.requestFocus();
            bandera = false;
            JOptionPane.showMessageDialog(panelCuerpo, "Digite el nombre del elemento", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            codInv = txtCodigo.getText();
            if (codInv.equals("")) {
                txtCodigo.requestFocus();
                bandera = false;
                JOptionPane.showMessageDialog(panelCuerpo, "Digite el código de inventario del elemento", "Advertencia", JOptionPane.WARNING_MESSAGE);
            } else {
                referencia = txtReferencia.getText();
                if (referencia.equals("")) {
                    txtReferencia.requestFocus();
                    bandera = false;
                    JOptionPane.showMessageDialog(panelCuerpo, "Digite la referencia del elemento", "Advertencia", JOptionPane.WARNING_MESSAGE);
                } else {
                    try {

                        cantidad = Integer.valueOf(txtCantidad.getText());
                        if (cantidad < 0) {
                            txtCantidad.requestFocus();
                            bandera = false;
                            JOptionPane.showMessageDialog(panelCuerpo, "Digite numeros positivos", "Advertencia", JOptionPane.WARNING_MESSAGE);
                        }
                    } catch (NumberFormatException e) {
                        bandera = false;
                        JOptionPane.showMessageDialog(panelCuerpo, "Digite cantidad del elemento", "Advertencia", JOptionPane.WARNING_MESSAGE);
                    }
                }

            }

        }
        return bandera;

    }

